"""Обработчик для команды /help."""

from aiogram import Router, types
from aiogram.filters import Command

router = Router()

HELP_MESSAGE = """
Привет! Я твой личный трекер активности. Вот как мной пользоваться:

**Основные команды:**
- /start или /menu: Вернуться в главное меню.
- /help: Показать это сообщение.

**Главное меню:**
Здесь ты найдеешь кнопки для управления активностями.

**1. Активности:**
Нажав на эту кнопку, ты увидишь список всех твоих активностей.
-   **Checkbox активности:** Нажимай на них, чтобы отметить выполнение (✅/☑️).
-   **Time активности:**
    -   Нажми на активность, чтобы запустить или остановить таймер (▶️/⏹️).
    -   Рядом с названием активности есть значок ✏️ (карандаш). Нажав на него, ты сможешь вручную ввести количество минут, которое ты потратил на эту активность.

**2. Добавить активность:**
Здесь ты можешь создать новую активность.
-   Придумай название.
-   Выбери тип: "Checkbox" (для однократных отметок) или "Time" (для отслеживания времени).

**3. Статистика:**
Просмотр статистики по твоим активностям за разные периоды (день, неделя, месяц).

**4. Скачать исходники:**
Позволяет скачать твои данные в формате Markdown за выбранный период.

Надеюсь, это поможет тебе максимально эффективно использовать бота!
"""


@router.message(Command("help"))
async def handle_help(message: types.Message):
    """Отправляет пользователю сообщение с инструкциями по использованию бота."""
    await message.answer(HELP_MESSAGE, parse_mode="Markdown")

